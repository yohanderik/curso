#MODULO = NUMERO * (10 ** QUANTIDADE DE ZERO)

#EX:
print(0.51 * (10 ** 1))                             #= 5.1
print(0.051 * (10 ** 2))                            #= 5.1
print(0.0050999999999999995 * (10 ** 3))            #= 5.1
print(0.00051 * (10 ** 4))                          #= 5.1
print(5.1e-05 * (10 ** 5))                          #= 5.1



#MODULO = NUMERO * (10 ** -QUANTIDADE DE ZERO)

#EX:
print(5.1 * (10 ** -1))             #= 0.51
print(5.1 * (10 ** -2))            #= 0.051
print(5.1 * (10 ** -3))           #= 0.0051
print(5.1 * (10 ** -4))          #= 0.00051
print(5.1 * (10 ** -5))         #= 0.000051